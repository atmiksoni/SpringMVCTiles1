package com.pizza;

public class PizzaDetail {

	private String pizzSize;

	private String pizzaBase;

	private String sauce;

	private String[] topping;

	private String cheese;

	public String getPizzSize() {
		return pizzSize;
	}

	public void setPizzSize(String pizzSize) {
		this.pizzSize = pizzSize;
	}

	public String getPizzaBase() {
		return pizzaBase;
	}

	public void setPizzaBase(String pizzaBase) {
		this.pizzaBase = pizzaBase;
	}

	public String getSauce() {
		return sauce;
	}

	public void setSauce(String sauce) {
		this.sauce = sauce;
	}

	public String[] getTopping() {
		return topping;
	}

	public void setTopping(String[] topping) {
		this.topping = topping;
	}

	public String getCheese() {
		return cheese;
	}

	public void setCheese(String cheese) {
		this.cheese = cheese;
	}

}
